<div class="principal_faq">
	
	<h4 id="faq_title">FAQ - Perguntas mais frequentes</h4>
	
	<hr class="linha">
	
	<strong>P: </strong>Aos preços apresentados acresce alguma taxa extra?
	<p></p>
	<strong>R: </strong>Os preços apresentados são os preços finais salvo as situações de chumbo ou falta a algum exame, situação em que será acrescido o valor dos mesmos.
	
	<hr class="linha">
	
	<strong>P: </strong>Qual a duração das aulas de condução e código?
	<p></p>
	<strong>R: </strong>Ambas as aulas têm duração de 60 minutos.

	<hr class="linha">
	
	<strong>P: </strong>Que documentos são necessários para fazer a inscrição na escola?
	<p></p>
	<strong>R: </strong>São necessários: 1 fotografia tipo passe; cartão de cidadão; atestado médico.<br>
	<p></p><i id="textoMenor">Caso seja menor será preciso uma autorização por parte dos pais.</i>

	<hr class="linha">
	
	<strong>P: </strong>O veículo que vou conduzir no exame de condução será o mesmo usado nas aulas?
	<p></p>
	<strong>R: </strong>Sempre que possível, será utilizado em exame o mesmo veículo da formação.
	
	<hr class="linha">
	
	<strong>P: </strong>Com que idade é que posso tirar a carta de ligeiros?
	<p></p>
	<strong>R: </strong>A carta de ligeiros pode ser obtida aos 18 anos, no entanto o candidato pode começar a formação até 6 meses antes de os completar.

