	<h4 id="quemsomos_title">Quem Somos?</h4>

	<div id="corpoTexto" class="text-justify">

		<p>A escola de condução Alpha foi fundada no ano 2016 através duma PME no âmbito do programa 2020.
		Pela qualidade do trabalho que desempenhamos e pela constituição da nossa equipa multidisciplinar temos conseguido estabelecer um vínculo sólido no mercado, permitindo até a expansão com duas escolas previstas para abrir em fevereiro de 2018.</p>
		 
		<p>A nossa missão é dar formação de excelência aos nossos alunos e não é por acaso que temos uma das melhores taxas de aprovação atual.
		Com vista à constante melhoria, aderimos e comprámos uma aplicação que irá potenciar a comunicação entre os alunos desta escola e os seus funcionários.
		Este é sem dúvida absoluta um factor diferenciador que demonstra a nossa visão de olhar, fazer e investir pelo aluno e para o aluno.</p>
		
		<p>Oferecemos programa formativo capaz de promover o melhor ensino.</p>
		
		<p>Equipa composta por instrutores profissionais e certificados a lecionar todo o tipo de categorias.
		Atualmente a nossa frota engloba Renault Clios, Audis A8, um veículo longo Scania e 2 motos BMW.</p>

		<p>Recentemente a escola investiu numa aplicação web responsiva, <strong>DriveAway</strong>, que irá facilitar a comunicação entre a nossa escola e os seus alunos, trazendo a nossa intituição para a era digital e garantindo assim mais um elemento diferenciador.</p>

